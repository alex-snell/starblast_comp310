#define FONT_SIZE (sizeof(font) / sizeof(font[0]))

static const Glyph *find_glyph(char c) {
    // Convert lowercase to uppercase
    if (c >= 'a' && c <= 'z') c -= 32;
    for (int i = 0; i < (int)FONT_SIZE; i++) {
        if (font[i].c == c) return &font[i];
    }
    return &font[0];  // space as fallback for unknown chars
}

static void draw_glyph(const Glyph *g, int x, int y, uint32_t color, int scale) {
    for (int row = 0; row < GLYPH_H; row++) {
        uint8_t bits = g->rows[row];
        for (int col = 0; col < GLYPH_W; col++) {
            if (bits & (1 << (GLYPH_W - 1 - col))) {
                // Draw a scale x scale block for this pixel
                for (int sy = 0; sy < scale; sy++) {
                    for (int sx = 0; sx < scale; sx++) {
                        video_set_pixel(x + col * scale + sx,
                                        y + row * scale + sy, color);
                    }
                }
            }
        }
    }
}

void text_draw_int(int num, int x, int y, uint32_t color, int scale) {
    char buf[16];
    int idx = 0;

    if (num < 0) {
        buf[idx++] = '-';
        num = -num;
    }

    if (num == 0) {
        buf[idx++] = '0';
    } else {
        // Build digits in reverse, then flip into final position
        char rev[16];
        int rcount = 0;
        while (num > 0 && rcount < 15) {
            rev[rcount++] = '0' + (num % 10);
            num /= 10;
        }
        while (rcount > 0) {
            buf[idx++] = rev[--rcount];
        }
    }
    buf[idx] = '\0';

    text_draw(buf, x, y, color, scale);
}

void text_draw(const char *str, int x, int y, uint32_t color, int scale) {
    int cursor_x = x;
    for (const char *p = str; *p; p++) {
        const Glyph *g = find_glyph(*p);
        draw_glyph(g, cursor_x, y, color, scale);
        cursor_x += GLYPH_STRIDE * scale;
    }
}

int text_width(const char *str, int scale) {
    int count = 0;
    for (const char *p = str; *p; p++) count++;
    if (count == 0) return 0;
    // N chars = N glyphs wide minus the trailing spacing
    return (count * GLYPH_STRIDE - 1) * scale;
}

int text_height(int scale) {
    return GLYPH_H * scale;
}
